### 色々な型

# 文字列型(Python, Twitterなどの文字)
# Pythonではstr型と呼ばれている
# ダブルクォーテーション(")かシングルクォーテーション(')で文字を囲んで文字を書く
"Python"
"Twitter"
# print関数で表示することが可能
print("Python")
print("Twitter")

# 整数型(1, 10, -1, 112などの整数)
# Pythonではint型と呼ばれている
# 何も囲まないで数字を書く
1
-1
112
# これもprint関数で表示可能
print(1)
print(-1)
print(112)

# 浮動小数点数型(3.1415, 1.7320508, -114.514などの小数)
# Pythonではfloat型と呼ばれている
3.1415
-114.514
# print関数で表示可能
print(3.1415)
print(-114.514)

# 論理型(True, Falseの2種類)
# Pythonではbool型と呼ばれている
# 真か偽かを表す
True
False
# print関数で表示可能
print(True)
print(False)

